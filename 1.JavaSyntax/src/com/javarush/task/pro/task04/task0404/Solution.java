package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int h = 0;
        while (h < 5) {
            int b = 0;
            while (b < 10) {
                System.out.print("Q");
                b++;
            }
            System.out.println();
            h++;
        }
    }
}